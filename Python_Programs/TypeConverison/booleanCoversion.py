# Boolean type conversion
print(bool(None))  # None keyword, it will print False

print(bool(0))  # Number equals to zero , it will print False

print(bool([]))  # Empty List, it will print False

print(bool({}))  # Empty dictionary, it will print False

print(bool(()))  # Empty Tuple, it will print False

print(bool(3))  # Number which are not zero, it will print True

print(bool([1, 3]))  # Non-Empty List, it will print True

print(bool(-6))  # Number equals to non-zero , it will print True
